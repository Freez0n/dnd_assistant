    @staticmethod
    def generate_npc_name(gender: str = None, race: str = None) -> str:
        if gender == "мужской":
            return random.choice(Generators.NPC_NAMES_MALE)
        elif gender == "женский":
            return random.choice(Generators.NPC_NAMES_FEMALE)
        else:
            return random.choice(Generators.NPC_NAMES_MALE + Generators.NPC_NAMES_FEMALE)
    
