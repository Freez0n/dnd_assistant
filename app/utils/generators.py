    @staticmethod
    def generate_encounter(biome: str) -> str:
        biome = biome.lower()
        if biome in Generators.BIOME_ENCOUNTERS:
            return random.choice(Generators.BIOME_ENCOUNTERS[biome])
        return random.choice(Generators.BIOME_ENCOUNTERS["лес"])
    
