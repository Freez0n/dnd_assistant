    @staticmethod
    def generate_treasure() -> dict:
        coins = {
            "медь": random.randint(0, 100),
            "серебро": random.randint(0, 50),
            "электрум": random.randint(0, 20),
            "золото": random.randint(0, 30),
            "платина": random.randint(0, 10)
        }
        item_count = random.randint(0, 3)
        items = []
        rarities = ["Обычный", "Необычный", "Редкий", "Очень редкий", "Легендарный"]
        item_types = ["Оружие", "Доспех", "Зелье", "Свиток", "Чудо-предмет", "Кольцо", "Посох"]
        
        for _ in range(item_count):
            items.append({
                "name": f"{'Магический' if random.random() > 0.5 else 'Обычный'} {random.choice(item_types)}",
                "rarity": random.choice(rarities),
                "description": "Описание предмета будет добавлено Мастером"
            })
        
        return {"coins": coins, "items": items}